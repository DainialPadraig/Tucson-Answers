/**
 * Process variables for search-results.tpl.php.
 *
 * @see search-result.tpl.php
 */
function tucson_answers_preprocess_search_result(&$variables) {
  // Remove user name and modification date from search results
  $variables['info'] = '';
	// Remove the word  "Keywords"
	$variables['snippet'] = str_replace('Keywords:', '', $variables['snippet']);
}

/*
 * Reset Page Title on home page
 */
function tucson_answers_preprocess(&$variables, $hook){
	// If front page
	if ($variables['is_front']) {
		switch ($hook) {
			case 'html':
				// Change the page title
				$variables['head_title'] = variable_get('site_name');
				break;
		}
	}
}

// remove a tag from the head for Drupal 7
function tucson_answers_html_head_alter(&$head_elements) {
  unset($head_elements['system_meta_generator']);
}

